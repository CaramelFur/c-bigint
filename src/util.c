void print_bits(void *data, size_t length)
{
  unsigned char *p = data;

  for (size_t i = 0; i < length; i++)
    for (size_t j = 0; j < 8; j++)
      printf("%d", (p[i] >> (7 - j)) & 1);
}

uint8_t hex2byte(char *hexpair)
{
  return (uint8_t)(hex2nibble(hexpair[0]) << 4) | hex2nibble(hexpair[1]);
}
